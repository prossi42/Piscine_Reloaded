int	ft_iterative_factorial(int nb)
{
	int	i;

	i = nb;
	if (i < 0 || i > 12)
		return (0);
	if (i == 0)
		return (1);
	while (i > 1)
	{
		nb = nb * (i - 1);
		i--;
	}
	return (nb);
}
